    /**
     * Prints vehicle reg. numbers and report IDs from pre check reports to the list 
     * of reports with defects in the service alerts tab.
     */
    public void showPreUseReportsDefects()
    { 
        Map<Integer, PreCheckReport> reports = flCoord.getReports();
        
        jListReportsWithDefects.setModel(listModelReportsWithDefects);         
        for(Integer each : reports.keySet())                 
        {         
            PreCheckReport report = reports.get(each);            
            String regNo =  report.getRegNo();
            if (report.hasAnyDefect() && !report.getWasServiced()
                && !listModelReportsWithDefects.contains(regNo + "-" + each))  
            {                                           
                listModelReportsWithDefects.addElement(regNo + "-" + each);      
            }
        }      
    }   
                
    /**
     * Print defected items and defect details from the selected pre use 
     * check report in pre use check defects panel in the service alerts tab.
     */
    public void showPreUseCheckReportDetails()
    {        
        listModelDefectedItems.clear();                                                                          
        jListDefectedItems.setModel(listModelDefectedItems);

        PreCheckReport report = getSelectedReportWithDefects();  // gets report that is selected in reports with defects  
            // check whether elements have defect if yes prints details 
        if (!report.getBreaks()){listModelDefectedItems.addElement("brakes");}        
        if (!report.getLights()){listModelDefectedItems.addElement("lights");}        
        if (!report.getMirrors()){listModelDefectedItems.addElement("mirrors");} 
        jTextFieldDriverName.setText(report.getName()); 
        jTextFieldReportMileage.setText(String.valueOf(report.getMileage()));
        jTextAreaShowDefectDetails.setText(report.getDefectDetails()); // gets defect details from pre check report and show it in defect details window.                       
    }
             
