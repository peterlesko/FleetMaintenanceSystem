    /**
     * Set the vehicle for service 
     */
    public void scheduleService()
    {
        String regNoInServSchedu = jTextFieldRegNoServiceAlerts.getText();  // gets reg. no. from service alerts text field.
        PreCheckReport report = getSelectedReportWithDefects(); 
        boolean reportDefectsChBox = jCheckBoxReportDefects.isSelected(); 

        List<String> defectedItems = new ArrayList<String>();          
        String defectDetails = "";  
          
        if (reportDefectsChBox && (!jListReportsWithDefects.isSelectionEmpty())
            && (!report.getRegNo().equals(regNoInServSchedu))) // checks that report check box and report with defect were 
                                                               // selected, and its reg.no is the same as reg. no in create schedule  
        {            
            jTextFieldHelpMessage.setText("Must select the same vehicle in reports");
        }
        else if (reportDefectsChBox && (!report.getRegNo().equals(regNoInServSchedu)))    
        {
            jTextFieldHelpMessage.setText("Must select the same vehicle in reports");
        }   
        else 
        {
            for(int i=0; i < listModelDefectedItems.getSize(); i++)
            {
                String item = (String) listModelDefectedItems.getElementAt(i);  
                defectedItems.add(item);
            }
            defectDetails = jTextAreaShowDefectDetails.getText();            
            String mech1 = jTextFieldMechName1.getText();  // get mechanic from input field
            String mech2 = jTextFieldMechName2.getText();  // get mechanic from input field
            String mech3 = jTextFieldMechName3.getText();  // get mechanic from input field
            String[] mechanics = {mech1,mech2,mech3};
            
            boolean regulService = jCheckBoxRegularService.isSelected();            
            boolean CVRTService = jCheckBoxCVRTService.isSelected();
            boolean reportDefects = jCheckBoxReportDefects.isSelected();                    
              
            flCoord.addScheduledService(regNoInServSchedu, defectedItems, defectDetails, 
                                        mechanics, regulService, CVRTService, reportDefects);  //adds service schedule                         
            resetAddMechanics();            
        }          
    }
                
    /**
     * Identifies the vehicle that is selected in one of the date alerts list 
     * in Service alerts tab and ask other method to print the supplied vehicle details.
     * @param valueIn
     */
    public void getSelectedVehicleInAlerts(String valueIn) 
    { 
        String regNo = "";            
        switch (valueIn)
        {
            case "RegServiceDate" : regNo = jListForRegService.getSelectedValue();        
                  jListCVRTExpireDate.clearSelection();
                  jListTaxExpireDate.clearSelection();break;
            case "CVRTExpDate" : regNo = jListCVRTExpireDate.getSelectedValue();
                  jListForRegService.clearSelection();
                  jListTaxExpireDate.clearSelection();break;
            case "TaxExpDate" : regNo = jListTaxExpireDate.getSelectedValue();
                  jListForRegService.clearSelection();
                  jListCVRTExpireDate.clearSelection();break;
        }            
        Map<String, Vehicle> vehicles = flCoord.getVehicles();        
        Vehicle vehicle = vehicles.get(regNo);
        
        if (regNo != null)  // without check it might give NPE.
        {
            showSelectedVehicleDetails(vehicle);   // request to print vehicle details
        }                        
    }
        
    /**
     * Prints details of the supplied vehicle to the vehicle details panel in the service tab, the 
     * details of the supplied vehicle.Can be used use to display vehicle from pre use check defect.
     * @param vehicleIn 
     */
    public void showSelectedVehicleDetails(Vehicle vehicleIn)  
    {
        jTextFieldBrandServiceAlerts.setText(vehicleIn.getBrand());  
        jTextFieldRegNoServiceAlerts.setText(vehicleIn.getRegNo());                     
        jTextFieldMileageServiceAlerts.setText(String.valueOf(vehicleIn.getMileage()));                                     
                 
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd.MMM.yyyy");  
        
        LocalDate regServiceDate = vehicleIn.getRegServiceDate();        
        jTextFieldRegServDateServiceAlerts.setText((regServiceDate).format(formatter));                
        
        LocalDate CVRTExpireDate = vehicleIn.getCVRTExpireDate();
        jTextFieldCVRTExpDateServiceAlerts.setText((CVRTExpireDate).format(formatter));    
        
        LocalDate TaxExpireDate = vehicleIn.getTaxExpireDate();
        jTextFieldTaxExpireDateServiceAlerts.setText((TaxExpireDate).format(formatter));     
        clearSelection();
    }

